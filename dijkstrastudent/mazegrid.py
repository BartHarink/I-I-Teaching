def createMazeGrid():
    height = len(maze)
    width = len(maze[0])
    grid = LimitedGrid(0, 0, width, height)

    for x in range(width):
        for y in range(height):
            char = maze[y][x]
            grid.setCell(x, y, 0 if char == '#' else 1)
    
    return grid

